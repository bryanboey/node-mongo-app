const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const blogSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    snippet: {
        type: String,
        required: true
    },
    body: {
        type: String,
        required: true
    }
}, { timestamps: true });

// should be a singular of the collection name Blog will look at blogs
const Blog = mongoose.model('Blog', blogSchema);
module.exports = Blog;